fun main (){

    for (i in 1..5){
        println("HELLO")
    }
    println()
    for (i in 1 until 5){
        println("WORLD")
    }
    println()
    for(i in 5 downTo 1){
        println("XD")
    }
    println("______________________________________")
    for(i in 10 downTo (1) step(2)){
        print("$i ")
    }
}
