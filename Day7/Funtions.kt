fun main(){
    println(addition(10,20))
    println(subs(20,10))
    println(multiplication(10,10))
    println(division(100,10))

}
fun addition(num1 : Int ,num2 : Int) :Int {
    return num1+num2
}
fun subs(num1: Int , num2: Int) :Int {
    return num1-num2
}
fun multiplication(num1: Int , num2: Int) :Int {
    return num1*num2;
}
fun division(num1: Int,num2: Int) :Int{
    return num1/num2;
}
